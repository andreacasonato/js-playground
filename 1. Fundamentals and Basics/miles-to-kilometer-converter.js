// Create a variable that contains a value in miles, convert it to kilometers,
// and log the value in kilometers in the following format:
// The distance of 130 miles is equal to 209.2142 kilometers.

let miles = 130;
let km = miles * 1.60934;

console.log(`The distance of ${miles} miles is ${km} kilometers.`);
// The distance of 130 miles is 209.2142 kilometers.