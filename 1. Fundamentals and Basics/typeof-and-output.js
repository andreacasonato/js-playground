// What are the types of these variables listed below? 
// Verify this with typeof and output the result to the console

let str1 = 'Laurence';
let str2 = "Svekis";
let val1 = undefined;
let val2 = null;
let myNum = 1000;

console.log(typeof str1, str1); // string Laurence
console.log(typeof str2, str2); // string Svekis
console.log(typeof val1, val1); // undefined undefined
console.log(typeof val2, val2); // object null
console.log(typeof myNum, myNum); // number 1000
